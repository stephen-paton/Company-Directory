/* Infobox Events =================================================================================================================================== */
/* Imports ===== */
// Libraries
import $ from 'jquery';

/* Functions ===== */
/* Event Setup ======================================================================================================================================
Description: Sets up DOM events */
export const eventSetup = (function () {

    /* ==============================================================================================================================================
    Infobox Events
    ================================================================================================================================================= */
    /* Close Infobox when Close Button is Clicked ===== */
    $('body').on('click', '.infobox .close', function () {

        $('.infobox').has(this).remove();

    });

})();
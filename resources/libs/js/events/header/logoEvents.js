/* Logo Events =================================================================================================================================== */
/* Imports ===== */
// Libraries
import $ from 'jquery';

/* Functions ===== */
/* Event Setup ======================================================================================================================================
Description: Sets up DOM events */
export const eventSetup = (function () {

    /* ==============================================================================================================================================
    Logo Events
    ================================================================================================================================================= */
    $('body').on('click', '.main-header .logo', function () {

        $(this).addClass('spin-forward-three-times');

        $(this).prop('disabled', true);

    });


    /* ==============================================================================================================================================
    Animation End Events
    ================================================================================================================================================= */
    /* Remove spin-right-three-times Class from Logo */
    $('body').on('animationend', '.main-header .logo', function () {

        if ($(this).hasClass('spin-forward-three-times')) {

            $(this).removeClass('spin-forward-three-times');

            $(this).prop('disabled', false);

            return;

        }

    });

})();